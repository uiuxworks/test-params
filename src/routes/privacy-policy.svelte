				1 · About this privacy policy & Pledge Sahha: is committed to protecting your privacy and
				safeguarding your personal information. We are committed to handling personal information in
				accordance with applicable privacy laws, including the New Zealand Privacy Act, Australian
				Privacy Act 1988 (Cth) and Australian Privacy Principles (APPs). The purpose of this Privacy
				Policy (Policy) is to inform you about the types of personal information we may collect and
				hold, how that information is used, and with whom that information may be disclosed. This
				Policy also outlines what you can do if you have concerns about this Policy or your privacy.
				Sahha pledges to always keep user data privacy as a top priority throughout the
				organisation, and the products and initiatives it develops.
			</p>
			<br />
			<p>
				2 · Definitions In this Policy: “personal information means information or an opinion about
				an identified individual or an individual who is reasonably identifiable. It does not matter
				whether the information or opinion is true or not, or whether it is recorded in a material
				form or not. “sensitive information” means information or an opinion about an individual’s
				racial or ethnic origin, political opinions, membership of a political association,
				religious beliefs or affiliations, philosophical beliefs, membership of a professional trade
				or association, membership of a trade union, sexual orientation or practices or criminal
				record that is also personal information. It also includes health information about an
				individual and genetic information about an individual that is not otherwise health
				information. “we”, “our” and “us” refers to Sahha:, Sahha HealthTech Inc. “you” and
				“yourself” refers to any person about whom we collect personal information.
			</p>
			<br />
			<p>
				3 · What kinds of personal information do we collect? We may collect personal information
				including, but not limited to your name, email address, geographic location, age, gender,
				sexual orientation, relationship status, height when you participate in one of our projects
				or social initiatives or sign up to use our services. We may also collect your personal
				information, such as your email address, if you subscribe to our mailing list, or contact
				us.
			</p>
			<br />
			<p>
				4 · How do we collect your personal information? We may collect personal information from
				you if you participate in one of our projects or initiatives in person or via one of our
				digital platforms. We may also collect your personal information through third party social
				media sites (eg Facebook) with your consent. Sometimes, we may collect your personal
				information from a third party, such as where someone who knows you provides us with your
				information so we can contact you. We may also collect your personal information from public
				sources. Collection can occur in a number of different ways, such as via online
				registration, through the use of our apps, social media, email, telephone, in person, and
				mail.
			</p>
			<br />
			<p>
				5 · How do we hold your personal information? We hold personal information in an electronic
				database which may be hosted by a third party service provider. We take physical and
				electronic security measures to safeguard personal information from loss, misuse,
				unauthorised access, modification, or disclosure. This may include restricting access to
				personal information, entering into confidentiality agreements with third parties and using
				current technological protection measures.
			</p>
			<br />
			<p>
				6 · What are the purposes for which we collect, hold, use, and disclose your personal
				information? We collect, hold, use and disclose personal information for the following
				purposes: responding to your enquiries and communications we receive about our projects and
				social initiatives; collecting feedback and developing, improving, managing, and
				administering our projects and social initiatives, including your participation in our
				projects and initiatives; and telling you about our projects and social initiatives (unless
				you have told us that you do not want this information). The information we collect from
				projects and social initiatives may also be provided to the general public for the purposes
				of developing and implementing social impact initiatives or outcomes. Information shared
				with the general public will always be de-identified, so we do not provide them with your
				personally identifiable information. Information shared with partnering organisations may
				include personally identifiable information. We may also collect, hold, use and disclose
				personal information for other purposes which are disclosed at the time of collection or
				which you consent to or are required by law.
			</p>
			<br />
			<p>
				7 · Who do we disclose personal information to? Your personal information may be disclosed
				to contractors and agents help us to run our projects and social initiatives. We may also
				disclose your personal information if it is required or authorised by law, where disclosure
				is necessary to prevent a threat to life, health or safety, or where we are otherwise
				permitted by the Privacy Act. We will never sell your personal information to anyone.
			</p>
			<br />
			<p>
				8 · How do we deal with sensitive information? Some of the information we collect may be
				sensitive information, such as information relating to religious affiliation and sexual
				orientation. We will only collect sensitive information with your consent and if that
				information is reasonably necessary for us to carry out at least one or more of our
				functions or activities. We will only use and disclose your sensitive information for the
				purpose for which it was provided or a directly related secondary purpose, unless agreed
				otherwise by you, or the use or disclosure of that sensitive information is required or
				authorised by law a court/tribunal order (eg preventing a serious threat to life, health or
				safety).
			</p>
			<br />
			<p>
				9 · Do we disclose personal information to overseas recipients? Our services are mainly
				provided from New Zealand and Australia. The only circumstance in which we disclose personal
				information to an overseas recipient is where your information stored in an electronic
				database operated by our service provider (currently Australia). We will take reasonable
				steps to ensure that overseas recipients do not breach the APPs in relation to your personal
				information.
			</p>
			<br />
			<p>
				10 · How can you access, update or seek correction of your personal information? You can
				contact us to ask for access to your personal information held by us, or request to have it
				updated or corrected. To protect your personal information, we will always confirm your
				identity before giving access or making any change. In limited circumstances, we may decline
				your request to access or update your personal information (eg once you have entered your
				information via one of our apps and that information has been de-identified, we are unable
				to give you access or make any change to that information). In such circumstance, we will
				provide you with a written reason for our decision. Generally, there is no cost for
				accessing your personal information unless the request is complex, resource intensive, or we
				incur additional costs in providing you with access. If there is a charge, it will be
				reasonable and we will let you know in advance to receive your approval before we proceed.
				If you wish to have your personal information removed from our records, you may request that
				your personal information be removed or destroyed. It may not always be possible to comply
				with your request.
			</p>
			<br />
			<p>
				11 · Can you deal with us anonymously? In most cases, you will be able to deal with us
				anonymously (eg by using an email address or username that does not identify you). There may
				be situations where it is not practicable for us to deal with you anonymously. In those
				situations, if we cannot collect personal information about you, we may not be able to
				provide you with our services or you may not be able to participate in our projects or
				social initiatives.
			</p>
			<br />
			<p>
				12 · How do you make a complaint? If you have any questions or concerns about this Policy or
				how your personal information is handled by us, you may contact us. We will aim to respond
				to your request or complaint promptly. We take all complaints seriously and are committed to
				a quick and fair resolution. You may also make a complaint to the Office of the Australian
				Information Commissioner (OAIC). If you do so, the OAIC may recommend that you try to
				resolve your complaint directly with us in the first instance. The OAIC can be contacted on
				1300 363 992 or via the Office of the Australian Information Commissioner website. The
				website also contains further information about making complaints relating to privacy.
			</p>
			<br />
			<p>
				13 · How do we change this Policy? We may change this Policy at any time without notice to
				you. We recommend that you visit this webpage regularly to keep up to date with any changes.
			</p>
			<br />
			<p>
				14 · How do you contact us? You can contact us any time via hello@sahha.ai. Do you have
				something you’d like to tell us about our policy or pledge? Please get in touch by visiting
				<a href="https://sahha.ai">https://sahha.ai</a>
			</p>

